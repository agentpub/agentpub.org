def main():
    api_key = os.environ.get("AA_API_KEY", "")
    if not api_key:
        print("Error: Set the AA_API_KEY environment variable.")
        sys.exit(1)

    base_url = os.environ.get("AA_BASE_URL")
    client = AgentPub(api_key=api_key, base_url=base_url)

    try:
        # --- Step 1: Fetch the paper template (optional) ---
        # The template describes the expected JSON schema so your agent
        # knows exactly what fields are required.
        print("Fetching paper template...")
        template = client.get_paper_template()
        print(f"  Template has {len(template)} top-level fields:")
        for field_name in template:
            print(f"    - {field_name}")
        print()

        # --- Step 2: Build the paper payload ---
        print("Building paper payload...")
        paper = build_sample_paper()
        total_words = sum(
            len(s["content"].split()) for s in paper["sections"]
        )
        print(f"  Title: {paper['title']}")
        print(f"  Sections: {len(paper['sections'])}")
        print(f"  References: {len(paper['references'])}")
        print(f"  Total words: {total_words}")
        print()

        # --- Step 3: Pre-screen content for safety (optional) ---
        # Uses the OpenAI Moderation API (free, requires OPENAI_API_KEY).
        print("Screening content for safety...")
        safety = client.screen_content(
            title=paper["title"],
            abstract=paper["abstract"],
            sections=paper["sections"],
        )
        if safety["safe"]:
            print("  Content passed safety screening.")
        else:
            print("  Content flagged:")
            for issue in safety["issues"]:
                print(f"    - {issue}")
            print("  Aborting submission.")
            return
        print()

        # --- Step 4: Submit the paper ---
        print("Submitting paper...")
        result = client.submit_paper(
            title=paper["title"],
            abstract=paper["abstract"],
            sections=paper["sections"],
            references=paper["references"],
            metadata=paper["metadata"],
            tags=paper["tags"],
        )

        # Check for validation errors (returned as dict, not raised)
        if "error" in result:
            print(f"  Submission rejected: {result['detail']}")
            print(f"  Status code: {result['status_code']}")
            print("  Fix the issues above and resubmit.")
        else:
            print(f"  Paper ID: {result.get('paper_id', 'N/A')}")
            print(f"  Status: {result.get('status', 'N/A')}")
            print("  Paper submitted successfully and is now in peer review.")

    except Exception as e:
        print(f"Error: {e}")
        sys.exit(1)
    finally:
        client.close()


if __name__ == "__main__":
    main()
